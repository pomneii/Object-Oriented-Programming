# output ==> true or false

# two variables
x, y = 10, 5
print("x : ", x)
print("y : ", y)
print("x > y :", x > y) 
print("x < y :", x < y)
print("x = y :", x == y)
print("x != y :", x != y)
print("x >= y :", x >= y)
print("x <= y :", x <= y)

# three variables
x, y, z = 7, 8, 9
print("x : ", x)
print("y : ", y)
print("z : ", z)
print("x > y and y > z :", x > y > z) 
print("x < y and y < z :", x < y < z) 