
# Operators ==> + - * / %

x = 10
y = 5

# Addition
print("Addition : ", x + y)

# Substraction
print("Substraction : ", x - y)

# Multiply
print("Multiple : ", x * y)

# Divide
print("Divide : ", x / y)
print("ปัดเศษลง : ", x // y)

# exponent
print("Exponent : ", x ** y)

# modulus 
print("Mod : ", x % y)